const findMaxConsecutiveOnes = function (nums) {
  let leftPtr = 0; // Left Pointer.
  let lastZero = -1; // Index location of last zero seen.
  let rightPtr = 0; // Current index location.
  let maxSeq = 0; // The maximum number of consecutive 1's if you can flip at most one 0.

  while (rightPtr < nums.length) {
    if (nums[rightPtr] === 0) {
      maxSeq = Math.max(rightPtr - leftPtr, maxSeq); // Get the max sequence.
      leftPtr = lastZero + 1; // Last zero flipped to one is now flipped back to zero.
      lastZero = rightPtr; // This zero bit is now flipped to one.
    }
    rightPtr++;
  }

  // Return the max sequence when we finished our array iteration.
  console.log(Math.max(rightPtr - leftPtr, maxSeq));
};

